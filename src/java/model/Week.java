package model;

/**
 *
 * @author trung
 */
public class Week {
    private String value;
    private String detail;

    public Week() {
    }
    
    public Week(String value, String detail) {
        this.value = value;
        this.detail = detail;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
    
}
